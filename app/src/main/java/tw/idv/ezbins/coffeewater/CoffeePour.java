package tw.idv.ezbins.coffeewater;

class CoffeePour {
    private int pour_times;
    private int coffee_weight;
    private int water_ratio;

    public int getPour_times() {
        return pour_times;
    }

    public void setPour_times(int pour_times) {
        this.pour_times = pour_times;
    }

    public int getCoffee_weight() {
        return coffee_weight;
    }

    public void setCoffee_weight(int coffee_weight) {
        this.coffee_weight = coffee_weight;
    }

    public int getWater_ratio() {
        return water_ratio;
    }

    public void setWater_ratio(int water_ratio) {
        this.water_ratio = water_ratio;
    }
}
